function getSpanischWord() {
    var randomNumber = Math.floor(Math.random() * (spanisch.length));
    var randomSpanischWord = spanisch[randomNumber];
    return randomSpanischWord;
}


function printQuestionSpa() {
    var gerWord = getSpanischWord();
    var wordCont = document.getElementById("spanword");
    var wordStr = `<p class="engwordspa">${gerWord.engwordspa}</p>
                  <p class="spaword">${gerWord.spaword}</p>`

    wordCont.innerHTML = wordStr;
    
    
}

//event listener to activate the click on button
document.getElementById("revealspa").addEventListener("click", printQuestionSpa, false);

